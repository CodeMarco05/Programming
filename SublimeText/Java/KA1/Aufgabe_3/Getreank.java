package KA1.Aufgabe_3;

public class Getreank {
    private String marke;
    private double preis;

    public Getreank(String marke, double preis){
        this.marke = marke;
        this.preis = preis;
    }

    public String getMarke(){
        return marke;
    }

    public double getPreis(){
        return preis;
    }

}
